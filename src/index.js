function Pizza({ pizzaObj }) {
  return (
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT!" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 10;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;

  return (
    <footer className="footer">
      {isOpen ? <Open closeHour={closeHour} /> : <Close openHour={openHour} />}
    </footer>
  );
  // React.createElement("footer", null, "We are currently open!");
}

function Open({ closeHour }) {
  return (
    <div className="order">
      <p>We are open untill {closeHour}:00 </p>
      <button className="btn">Order Now!</button>
    </div>
  );
}

function Close({ openHour }) {
  return <p>We are currently closed...Opening at {openHour}:00 !</p>;
}
